// BOM- Browser Object Model
// The BOM provides methods and properties to interact with the browser and its components, such as the window, document, history, and navigator objects. It allows developers to manipulate the browser's behavior and access information about the user's environment.

// Example of using BOM to manipulate the browser window
// Open a new window
console.log(window); // Output: Window object

let data = prompt("Enter your name:"); // Output: A prompt dialog asking the user to enter their name
console.log(data); // Output: The name entered by the user

// alert example
alert("Welcome to the BOM tutorial!"); // Output: An alert dialog with the message "Welcome to the BOM tutorial!"     